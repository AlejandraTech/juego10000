package com.alejandrapazrivas.juego10000.domain.model

/**
 * Enum que define los niveles de dificultad disponibles para los bots.
 * Cada nivel determina diferentes estrategias y comportamientos de juego.
 */
enum class BotDifficulty {
    BEGINNER,    // Nivel principiante: estrategia más conservadora y predecible
    INTERMEDIATE, // Nivel intermedio: equilibrio entre riesgo y seguridad
    EXPERT       // Nivel experto: estrategia más agresiva y optimizada
}
